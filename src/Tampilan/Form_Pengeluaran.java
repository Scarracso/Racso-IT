    protected void autonumber(){
 try{
 String sql = "SELECT ID_pengeluaran FROM data_pengeluaran order by ID_pengeluaran asc";
 Statement stat = conn.createStatement();
 ResultSet rs=stat.executeQuery(sql);

 while(rs.next()){
 String ID_pengeluaran = rs.getString("ID_pengeluaran").substring(2);
 int nomor = Integer.parseInt(ID_pengeluaran) + 1;
 String nol = "";

 if (nomor<10)
 {nol = "000";}
 else if (nomor<100)
 {nol = "00";}
 else if (nomor<1000)
 {nol = "";}

 id_pengeluaran.setText("PG" + nol + nomor);
 }
 }
 catch (Exception e){
      JOptionPane.showMessageDialog(null,"Penomoran Salah" + e);
 }
}
    
