    private void SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SimpanActionPerformed
        // TODO add your handling code here:
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd (HH:mm:ss)");
        String fd = sdf.format(tanggal.getValue());

        String zsql = "insert into data_pengeluaran values (?,?,?,?,?,?,?,?,?,?)";
        try{
            int t = Table.getRowCount();
            for(int i=0; i < t ; i++)
            {
                String ID_pemasukan = Table.getValueAt(i, 0).toString();
                String Nama_Pemasukan = Table.getValueAt(i, 1).toString();
                String Jenis_Pemasukan = Table.getValueAt(i, 2).toString();
                String Banyak_Pengeluaran = Table.getValueAt(i, 3).toString();
                String Keterangan = Table.getValueAt(i, 4).toString();

                PreparedStatement stat2 = conn.prepareStatement(zsql);
                stat2.setString(1, id_pengeluaran.getText());
                stat2.setString(2, fd);
                stat2.setString(3, id_admin.getText());
                stat2.setString(4, id_ang.getText());
                stat2.setString(5, nma_ang.getText());
                stat2.setString(6, ID_pemasukan);
                stat2.setString(7, Nama_Pemasukan);
                stat2.setString(8, Jenis_Pemasukan);
                stat2.setString(9, Banyak_Pengeluaran);
                stat2.setString(10, Keterangan);
                stat2.executeUpdate();
            }
            JOptionPane.showMessageDialog(null, "data berhasil disimpan");
        }catch(Exception e)
        {
            System.out.println("Error : "+e);
        }
        kosong();
        autonumber();
        removeTable();

    }//GEN-LAST:event_SimpanActionPerformed

